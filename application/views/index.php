	<p>It is a privilege and an honour to welcome you to the World Psychiatric Association(WPA) regional meeting in Manila, Philippinesto be held from 2nd to 4th February 2016 in joint collaboration with Philippines Psychiatry Association on the theme of “Psychiatry and Society in the 21st Century”.</p>
	<p>This meeting is the first ever WPA meeting to be held in the Philippines. WPA is committed to sharing experiences –both clinical and research evidence across the globe with mutual learning on matters of interest to improve patient care and engage communities in improving mental health and managing mental illness in the best way possible. We are acutely aware that in many less developed countries, mental illness is treated in primary care with somewhat limited access to specialist care. In addition cultural models and cultural values play a major role in establishing and accessing services. With downturn in global economy and resulting pressures, it is important that we look at shrinking resources in the face of increasing demands. To this end, this Conference provides an ideal forum to share clinical experiences, new research and policy matters with various topics across the life span and across different specialties. Manila offers an ideal place in the region to provide academic and social activities to interact with colleagues and meet policy makers, stakeholders and researchers. Well known and well-respected researchers, policy makers and clinicians will cover a range of topics.</p>
	<p>This Congress offers a truly exciting opportunity in the region to develop academic and research links and initiate and engage in dialogue to develop local services in the context of global changes. </p>
	<p>I look forward to meeting you and interacting with you in an exciting city and location.</p>
