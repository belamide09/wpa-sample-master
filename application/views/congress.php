<p><strong>Dear Colleagues and Friends,</strong></p>
<p>The Philippine Psychiatric Association warmly welcomes you to Manila, Philippines as we host the World Psychiatric Association Regional Meeting on Feb. 4-6, 2016.
</p>
<p>February is a pleasant time to come to Manila,   when the weather is cool and dry.  We have chosen the Philippine International Convention Center as the congress venue, perched on reclaimed ground with an arresting view of the famous Manila Bay sunset.  It promises to be an exciting encounter of mental health professionals in an equally exciting environment.
</p>
<p>The theme “Psychiatry and Society in the 21st Century” highlights the various  growth areas in the science and art of  psychiatry practice :   neuroscience, psychopharmacology, brain imaging and advances in technology  in the context of  environmental and cultural challenges unique to our region.   It is our hope that the Manila meeting be a venue for scientific exchange, new learning and a showcase of best practices in Asia and the Pacific Rim.
</p>
<p>In true Filipino spirit, the Manila meeting also includes a social program that will enhance camaraderie and collaboration amongst ourselves. We invite you to a stimulating intellectual exchange in a historic city where symbols of our colonial past mix with our contemporary present.  
</p>
<p>We have invited world renowned scientists, clinicians and thought leaders in mental health to share their expertise.  We also look forward to presentations of innovative work from various WPA member countries, especially young researchers in the region.
</p>

<p>Come to Manila in February and see for yourself why it is truly more fun in the Philippines.</p>

