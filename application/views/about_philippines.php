      					<p>The Philippines is an archipelagic Democratic state that is a melting pot of Eastern and Western cultures. Prior to being colonized by the Spaniards in 1565, it was already populated by a diverse society that traded with neighbouring cultures. </p>
      					<p>The Philippines is an archipelagic Democratic state that is a melting pot of Eastern and Western cultures. Prior to being colonized by the Spaniards in 1565, it was already populated by a diverse society that traded with neighbouring cultures. </p>
      					<p>After the Philippine Revolution in 1898, the country gained temporary liberty, only to be colonised again by the United States of America. In 1916, the US government formally promised the country independence through the Jones Act. </p>
      					<p>The Philippines was then extensively damaged by the two World Wars because of American involvement in its affairs. However, it was able to recover after peace was restored across the globe. </p>	
      					<p>It came under dictatorship in 1972 with the rule of President Ferdinand Marcos, but democracy was restored through a peaceful revolution in 1986, where President Corazon Cojuangco-Aquino.</p>
      					<p> Today, the country enjoys a robust economic outlook, the darling of business investors  and a vibrant society fully engaged with the global community, especially through technology. Makati, one of the major cities in the Metro, was declared the selfie capital of the world, besting even New York , in a recent poll by Time Magazine. </p>
      					<p>Outside the urban centres, there is a lot to appeal to the nature lover. Because it is an archipelago, the Philippines is home to numerous magnificent beaches. Trekkers who love mountaineering can also find a mountain or two to climb here. </p>

